public class FloatCalculations {
    public static void main(String[] args){
        Float nbr1 = 12.6f;
        Float nbr2 = 32.54f;

        Float sum = nbr1 + nbr2;
        Float difference =  nbr2 - nbr1;
        Float product = nbr1 * nbr2;

        System.out.println("My 2 floating point numbers are: 12.6 and 32.54");
        System.out.println("The sum of the 2 floating point numbers is: " + sum);
        System.out.println("The difference of the 2 floating point numbers is: " + difference);
        System.out.println("The product of the 2 floating point numbers is: " + product);
    }
}
