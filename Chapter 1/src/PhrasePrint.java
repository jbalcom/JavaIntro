public class PhrasePrint {
    public static void main(String[] args){
        System.out.println("(a.)");
        System.out.println("Deeds not Words");
        System.out.println();

        System.out.println("(b.)");
        System.out.println("Deeds");
        System.out.println("not");
        System.out.println("Words");
        System.out.println();

        System.out.println("(c.)");
        System.out.println(" ---------------------------");
        System.out.println("|                           |");
        System.out.println("|      Deeds not Words      |");
        System.out.println("|                           |");
        System.out.println(" ---------------------------");
    }
}
